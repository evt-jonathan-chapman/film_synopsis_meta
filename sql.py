SQL_CURRENT_SYNOPSES = '''
    SELECT 
        e.*  EXCLUDE (TITLE, SYNOPSES_ID, LOADED_TIMESTAMP), 
        COALESCE(m.MOVIE_NAME, f.FILM) AS TITLE, 
        COALESCE(m.SYNOPSIS, f.FILM_DESC) AS SYNOPSIS
    FROM ENT_FORECAST_PRD.CURATED.FILM_SYNOPSES_EXTRACTED AS e
    LEFT JOIN EDW_ENT_PRD.CURATED.DIM_VH_FILM AS f ON f.FILM_ID = e.FILM_ID
    LEFT JOIN EDW_ENT_PRD.CURATED.S2S_IHUB_MOVIE_AU AS m ON m.MOVIE_CODE = f.FILM_HO_CODE
'''

SQL_STAGING_TABLE = 'ENT_FORECAST_PRD.STAGING.FILM_SYNOPSES_EXTRACTED'

SQL_DELETE_STAGING_DEDUPE = '''
    DELETE FROM ENT_FORECAST_PRD.STAGING.FILM_SYNOPSES_EXTRACTED 
    WHERE FILM_ID IN (
        SELECT FILM_ID
        FROM (
            SELECT
                FILM_ID,
                ROW_NUMBER() OVER (
                    PARTITION BY FILM_ID
                    ORDER BY LOADED_TIMESTAMP DESC, FILM_ID
                ) AS rn
            FROM ENT_FORECAST_PRD.STAGING.FILM_SYNOPSES_EXTRACTED 
        )
        WHERE rn > 1
    );
'''

