SQL_MERGE_STAGING_TO_CURATED = '''
    MERGE INTO ENT_FORECAST_PRD.CURATED.FILM_SYNOPSES_EXTRACTED AS t
    USING ENT_FORECAST_PRD.STAGING.FILM_SYNOPSES_EXTRACTED AS s
    ON t.FILM_ID = s.FILM_ID
    WHEN MATCHED
        THEN UPDATE 
            ALL BY NAME 
    WHEN NOT MATCHED 
        THEN INSERT 
            ALL BY NAME;
'''
